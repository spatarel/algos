template<int SIZE>
class ReadStream {
private:
  FILE* stream;

  int pos;
  char buffer[SIZE + 1];

  void readBuffer() {
    int readSize = fread(this->buffer, sizeof(char), SIZE, this->stream);
    this->buffer[readSize] = 0;
    this->pos = 0;
  }

  void advance() {
    this->pos++;
    if (this->pos == SIZE) {
      readBuffer();
    }
  }

  void retreat() {
    this->pos--;
  }
  char getChar() {
    return this->buffer[this->pos];
  }

public:
  ReadStream(FILE* stream = stdin) {
    this->stream = stream;
    this->buffer[SIZE - 1] = 0;
    this->pos = SIZE - 1;
  }

  char nextChar() {
    advance();
    return getChar();
  }

  int nextUnsignedInt() {
    do {
      advance();
    } while (!isDigit[(int)getChar()]);
    int answer = 0;
    do {
      answer = answer * 10 + (getChar() - '0');
      advance();
    } while (isDigit[(int)getChar()]);
    retreat();
    return answer;
  }

  int nextString(char* answer) {
    do {
      advance();
    } while (isWhiteSpace[(int)getChar()]);
    int length = 0;
    do {
      answer[length] = getChar();
      length++;
      advance();
    } while (!isWhiteSpace[(int)getChar()]);
    answer[length] = 0;
    return length;
  }
};

int main() {
  freopen("algsort.in", "r", stdin);
  ReadStream<100000> reader;
  int N = reader.nextUnsignedInt();
  while (N > 0) {
    printf("%d ", reader.nextUnsignedInt());
    N--;
  }
  char string[100];
  reader.nextString(string);
  printf("\n%s", string);
  reader.nextString(string);
  printf("\n%s\n", string);
  return 0;
}
