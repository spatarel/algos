template<int SIZE>
class ReadStream {
private:
  FILE* stream;

  char buffer[SIZE];
  char* begin;
  char* end;

  void readBuffer() {
    this->begin = this->buffer;
    this->end = this->begin + fread(this->buffer, sizeof(char), SIZE, this->stream);
  }

  char currentChar() {
    return *this->begin;
  }

  void advance() {
    this->begin++;
    if (this->begin == this->end)
      this->readBuffer();
  }

public:
  ReadStream(FILE* stream = stdin) {
    this->stream = stream;
    this->begin = NULL;
    this->end = NULL;
    this->readBuffer();
  }

  char nextChar() {
    char answer = this->currentChar();
    this->advance();
    return answer;
  }

  int nextUnsignedInt() {
    while (isWhiteSpace[(int)this->currentChar()])
      this->advance();
    int answer = 0;
    while (isDigit[(int)this->currentChar()]) {
      answer *= 10;
      answer += this->currentChar() - '0';
      this->advance();
    }
    return answer;
  }

  int nextString(char* answer) {
    while (isWhiteSpace[(int)this->currentChar()])
      this->advance();
    int length = 0;
    while (!isWhiteSpace[(int)this->currentChar()]) {
      answer[length] = this->currentChar();
      length++;
      this->advance();
    }
    answer[length] = 0;
    return length;
  }
};

